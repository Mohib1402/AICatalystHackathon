export function getRiskLevel(score: number): RiskLevel {
  if (score >= 76) return RiskLevel.CRITICAL;
  if (score >= 51) return RiskLevel.HIGH;
  if (score >= 26) return RiskLevel.MEDIUM;
  return RiskLevel.LOW;
}

export function getPatternsByCategory(category: AttackCategory): AttackPattern[] {
  return ATTACK_PATTERNS.filter(p => p.category === category);
}

export function getPatternById(id: string): AttackPattern | undefined {
  return ATTACK_PATTERNS.find(p => p.id === id);
}

export function getCategoryCounts(): Record<AttackCategory, number> {
  const counts: Record<string, number> = {};
  for (const category of Object.values(AttackCategory)) {
    counts[category] = getPatternsByCategory(category).length;
  }
  return counts as Record<AttackCategory, number>;
}
